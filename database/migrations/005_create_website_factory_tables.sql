  UNIQUE KEY uq_factory_exports_download_token (download_token),
  KEY idx_factory_exports_project_id (factory_project_id),
  KEY idx_factory_exports_revision_id (revision_id),
  KEY idx_factory_exports_status (export_status),
  KEY idx_factory_exports_expires_at (expires_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
